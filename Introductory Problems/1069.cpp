void Solve() {
    string st;
    cin >> st;
    char last = '_';
    int ans = 0, cnt = 0;
    for (char &c : st) {
        if (c == last) cnt++;
        else {
            ans = max(ans, cnt);
            last = c;
            cnt = 1;
        }
    }
    ans = max(ans, cnt);
    cout << ans;
}

